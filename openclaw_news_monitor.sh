def extract_domain(url):
    """提取域名"""
    if not url:
        return ""
    # 移除协议和路径
    domain = url.replace("https://", "").replace("http://", "").split("/")[0]
    # 移除 www.
    if domain.startswith("www."):
        domain = domain[4:]
    return domain.lower()

def is_high_quality_domain(url):
    """检查是否为高质量域名"""
    domain = extract_domain(url)
    
    # 检查黑名单
    for bad_domain in LOW_QUALITY_DOMAINS:
        if bad_domain in domain:
            return False
    
    # 检查白名单
    for good_domain in HIGH_QUALITY_DOMAINS:
        if good_domain in domain:
            return True
    
    # 默认中等质量
    return "medium"

def filter_low_quality_content(content):
    """过滤低质量内容"""
    if not content:
        return ""
    
    # 移除常见的AI生成标记
    ai_markers = [
        "As an AI language model",
        "I am an AI assistant",
        "generated by AI",
        "AI-generated content",
        "created by artificial intelligence"
    ]
    
    for marker in ai_markers:
        if marker.lower() in content.lower():
            return ""
    
    # 移除过短的内容（可能不完整）
    if len(content.strip()) < 50:
        return ""
    
    return content

def translate_if_needed(text):
    """简单的关键词翻译（实际使用中可集成翻译API）"""
    # 这里只是示例，实际应该调用翻译API
    translations = {
        "OpenClaw": "OpenClaw",
        "AI agent": "AI代理",
        "skills": "技能",
        "tutorial": "教程",
        "update": "更新",
        "release": "发布",
        "community": "社区",
        "GitHub": "GitHub"
    }
    
    for eng, chi in translations.items():
        text = text.replace(eng, chi)
    
    return text

