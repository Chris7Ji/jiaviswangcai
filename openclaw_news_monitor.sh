            },
            timeout=30
        )
        
        if response.status_code == 200:
            data = response.json()
            if "results" in data:
                for result in data["results"]:
                    # 质量评估
                    url = result.get("url", "")
                    domain_quality = is_high_quality_domain(url)
                    
                    # 过滤低质量内容
                    content = filter_low_quality_content(result.get("content", ""))
                    if not content:
                        continue
                    
                    # 计算质量分
                    quality_score = 0
                    if domain_quality == True:
                        quality_score = 1.0  # 高质量域名
                    elif domain_quality == "medium":
                        quality_score = 0.7  # 中等质量
                    else:
                        quality_score = 0.3  # 低质量
                    
                    # 内容长度加分
                    content_length = len(content)
                    if content_length > 500:
                        quality_score += 0.2
                    elif content_length > 200:
                        quality_score += 0.1
                    
                    # 更新结果
                    result["quality_score"] = quality_score
                    result["domain_quality"] = domain_quality
                    result["filtered_content"] = content
                    
                    all_results.append(result)
                
                print(f"    ✅ 获取 {len(data['results'])} 条结果")
        else:
            print(f"    ❌ 请求失败: {response.status_code}")
    except Exception as e:
